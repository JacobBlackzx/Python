x = 1
y = 100

# Condição IF

if x > y:
    print("x é maior do que y")

if y > x:
    print("y é maior do que x")

# Condição ELSE

if x > y:
    print("x maior do que y")
else:
    print("x é maior do que y")

# Condição ELIF

A = 5
B = 10

if A == B:
    print("Número igual")
elif B > A:
    print("B é maior do que A")
else:
    print("numeros diferentes")